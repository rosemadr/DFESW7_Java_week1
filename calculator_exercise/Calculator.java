package com.qa.calculator_exercise;

public class Calculator {
	
	public int add(int x, int y) {
		int z = x + y;
		return z;
		
	}
	
	public int multiply(int x, int y) {
		int z = x * y;
		return z;
	}
	
	public int subtract(int x, int y) {
		int z = x - y;
		return z;
	}
	
	public double divide(double x, double y) {
		
		double z = x / y;			
		return z;
		
	}
}

